package com.kunning.commons.Test;

public class Binary {
	public static void main(String[] args) {
		int i = -4;
		System.out.println("移位前 i = " + i);
		System.out.println("移位前二进制 i = " + Integer.toBinaryString(i));
		System.out.println("移位前二进制 i = " + Integer.toBinaryString(4));//二进制表示
		System.out.println();
	}
}
